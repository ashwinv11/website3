<template>
  <article :key="project.slug" class="project">
    <div v-if="!project">Loading...</div>

    <div
      class="app__inner-content project__content--photography"
      v-else-if="project.slug === 'photography'"
    >
      <h1>{{ project.title }}</h1>
      <h3>{{ project.date }} // {{ project.category }}</h3>
      <h4 v-html="project.body"></h4>

      <masonry
        :cols="{ default: 3, 768: 2, 576: 1 }"
        :gutter="{ default: '2vmin' }"
      >
        <div class="project__photo" v-for="photo in photos" :key="photo.id">
          <progressive-img
            :src="photo.urls.regular"
            :placeholder="photo.urls.thumb"
          />
        </div>
      </masonry>
    </div>

    <div class="app__inner-content project__content" v-else>
      <h1>{{ project.title }}</h1>
      <h3>{{ project.date }} // {{ project.category }}</h3>
      <h4 v-if="tags !== ''">Tech Used &#8594; {{ tags }}</h4>
      <img
        v-if="project.imageAlt"
        :src="imageURL"
        :alt="project.imageAlt"
        :class="imgClass"
      />
      <div v-html="project.body"></div>
      <h3 v-if="project.link">
        <a :href="project.link">Link &#8594;</a>
      </h3>
    </div>
  </article>
</template>

<script lang="ts">
import Vue from 'vue'
import VueMasonry from 'vue-masonry-css'
import { unsplash } from '@/utils'
import { toJson } from 'unsplash-js'

import { Projects } from '@/data/projects'
import { Project } from '@/interfaces'

Vue.use(VueMasonry)

const findProject = (slug: string): any => {
  return Projects.find(project => {
    return project.slug === slug
  })
}

const debounce = (func: () => void, wait = 50) => {
  let h: any
  return () => {
    clearTimeout(h)
    h = setTimeout(() => func(), wait)
  }
}

export default Vue.extend({
  name: 'Project',
  data() {
    return {
      project: {} as Project,
      photos: [],
      canLoadMore: true,
      currentPage: 1,
    }
  },
  metaInfo() {
    const vm = this as any
    return {
      title: `Ashwin Vaswani // ${vm.project.title}`,
    }
  },
  watch: {
    $route(to, from) {
      this.project = findProject(to.params.slug)
      this.loadPhotos()
    },
  },
  beforeRouteEnter(to, from, next) {
    next(vm => {
      const VM = vm as any
      VM.setData(findProject(to.params.slug))
    })
  },
  mounted() {
    this.scroll()
  },
  computed: {
    imageURL(): string {
      return `${process.env.BASE_URL}images/${this.project.imageFile}`
    },
    imgClass(): object {
      return this.project.meta === undefined
        ? {}
        : { 'img--tall': this.project.meta.tallImage }
    },
    tags(): string {
      return this.project.tags === undefined
        ? ''
        : this.project.tags.join(' // ')
    },
  },
  methods: {
    setData(data: Project): void {
      this.project = data
      this.loadPhotos()
    },
    loadPhotos(): void {
      if (this.project.slug === 'photography' && this.canLoadMore) {
        const perPage = 10

        unsplash.users
          .photos('ashwinv11', this.currentPage, perPage, 'latest', false)
          .then(toJson)
          .then((response: any) => {
            if (response.length !== perPage) {
              this.canLoadMore = false
            }
            this.photos = this.photos.concat(response)
          })
      }
    },
    scroll() {
      const el = document.getElementsByClassName(
        'app__content',
      )[0] as HTMLElement

      el.onscroll = debounce(() => {
        if (
          el.scrollTop >= el.scrollHeight - el.offsetHeight - 800 &&
          this.canLoadMore
        ) {
          this.currentPage++
          this.loadPhotos()
        }
      }, 100)
    },
  },
})
</script>

